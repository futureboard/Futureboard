import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const electronRoot = path.resolve(__dirname, "..");
const workspaceRoot = path.resolve(electronRoot, "..", "..");
const source = path.join(workspaceRoot, "packages", "shared", "src", "menu", "menuItems.ts");
const targetDir = path.join(electronRoot, "src", "generated");
const target = path.join(targetDir, "menuItems.ts");

fs.mkdirSync(targetDir, { recursive: true });
const contents = fs.readFileSync(source, "utf8");
fs.writeFileSync(
  target,
  [
    "// Auto-generated by apps/electron/scripts/sync-shared-menu.mjs.",
    "// Do not edit this file directly; edit packages/shared/src/menu/menuItems.ts.",
    contents,
  ].join("\n"),
);

console.log("[sync-shared-menu] synced packages/shared/src/menu/menuItems.ts");
