// Build unified entry map (menu first, then components; components override duplicates)
const entries = new Map();
for (const e of menuEntries) entries.set(e.key, e.en);
for (const [key, en] of components) entries.set(key, en);

const sortedKeys = [...entries.keys()].sort();

function escapeFtlValue(s) {
  // Fluent multiline / special chars
  if (s.includes("\n")) {
    return '"""\n' + s + '\n"""';
  }
  return s;
}

function writeFtl(locale, getValue) {
  const lines = [
    `# Futureboard Studio — ${locale}`,
    `# Extracted from SphereUIComponents + native-menu.json`,
    `# Generated: ${new Date().toISOString().slice(0, 10)}`,
    "",
    "## Application Menu",
    "",
  ];

  let lastSection = "## Application Menu";
  for (const key of sortedKeys) {
    let section = "## Components";
    if (key.startsWith("menu.")) section = "## Application Menu";
    else if (key.startsWith("settings.")) section = "## Settings";
    else if (key.startsWith("wizard.") || key.startsWith("template.") || key.startsWith("add-track.")) section = "## Dialogs";
    else if (key.startsWith("plugin")) section = "## Plugins";
    else if (key.startsWith("context.")) section = "## Context Menus";
    else if (key.startsWith("browser.") || key.startsWith("search.")) section = "## Browser";
    else if (key.startsWith("inspector.") || key.startsWith("mixer.") || key.startsWith("timeline.") || key.startsWith("track-header.") || key.startsWith("bottom-panel.") || key.startsWith("panel.")) section = "## Panels & Timeline";
    else if (key.startsWith("status.") || key.startsWith("error.") || key.startsWith("dialog.") || key.startsWith("project.")) section = "## Status & Errors";
    else if (key.startsWith("chrome.") || key.startsWith("transport.") || key.startsWith("window.")) section = "## App Chrome";

    if (section !== lastSection) {
      lines.push("", section, "");
      lastSection = section;
    }
    const value = getValue(key, entries.get(key));
    lines.push(`${key} = ${escapeFtlValue(value)}`);
  }

  const outPath = path.join(root, `packages/shared/locales/${locale}/app.ftl`);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, lines.join("\n") + "\n", "utf8");
  console.log(`Wrote ${outPath} (${sortedKeys.length} keys)`);
}

// Write en-US (source)
writeFtl("en-US", (_key, en) => en);

console.log(`Total keys: ${sortedKeys.length}`);
