#!/usr/bin/env node
/**
 * Generate app.ftl locale files from SphereUIComponents strings + native-menu.json
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const menu = JSON.parse(
  fs.readFileSync(path.join(root, "packages/shared/generated/native-menu.json"), "utf8")
);

const menuEntries = [];
function walkMenu(items) {
  for (const item of items || []) {
    if (item.label && item.id) {
      menuEntries.push({ key: "menu." + item.id.replace(/\./g, "-"), en: item.label });
    }
    walkMenu(item.children);
  }
}
for (const m of menu.menus) {
  menuEntries.push({ key: "menu." + m.id, en: m.label });
  walkMenu(m.items);
}

