/**
 * Generate zh-CN.json, ja-JP.json, th-TH.json from unique English UI strings.
 * Keys are exact English source strings (Fluent placeholders preserved).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

/** @returns {string[]} */
function readEnglishKeys() {
  const uniquePath = path.join(__dirname, "unique-en.txt");
  if (fs.existsSync(uniquePath)) {
    return fs
      .readFileSync(uniquePath, "utf8")
      .split(/\r?\n/)
      .filter((line) => line.length > 0);
  }
  const entriesPath = path.join(__dirname, "entries.json");
  const entries = JSON.parse(fs.readFileSync(entriesPath, "utf8"));
  const seen = new Set();
  const keys = [];
  for (const [, en] of entries) {
    if (!seen.has(en)) {
      seen.add(en);
      keys.push(en);
    }
  }
  return keys;
}

