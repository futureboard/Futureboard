function buildMaps() {
  const zhCN = {};
  const jaJP = {};
  const thTH = {};
  for (const [en, zh, ja, th] of TRIPLES) {
    zhCN[en] = zh;
    jaJP[en] = ja;
    thTH[en] = th;
  }
  return { zhCN, jaJP, thTH };
}

function main() {
  const keys = readEnglishKeys();
  const { zhCN, jaJP, thTH } = buildMaps();

  const missing = keys.filter((k) => !(k in zhCN));
  const extra = Object.keys(zhCN).filter((k) => !keys.includes(k));
  if (missing.length) {
    console.error(`Missing translations for ${missing.length} key(s):`);
    for (const k of missing.slice(0, 20)) console.error(`  - ${k}`);
    if (missing.length > 20)
      console.error(`  ... and ${missing.length - 20} more`);
    process.exit(1);
  }
  if (extra.length) {
    console.warn(
      `Warning: ${extra.length} extra translation key(s) not in source list`,
    );
  }

  const outputs = [
    ["zh-CN.json", zhCN],
    ["ja-JP.json", jaJP],
    ["th-TH.json", thTH],
  ];

  for (const [filename, map] of outputs) {
    const outPath = path.join(__dirname, filename);
    const ordered = {};
    for (const k of keys) {
      ordered[k] = map[k];
    }
    fs.writeFileSync(outPath, JSON.stringify(ordered, null, 2) + "\n", "utf8");
    const count = Object.keys(ordered).length;
    console.log(`${filename}: ${count} keys`);
    if (count !== keys.length) {
      console.error(`Expected ${keys.length} keys, got ${count}`);
      process.exit(1);
    }
  }

  if (keys.length !== 677) {
    console.warn(`Note: source has ${keys.length} keys (expected 677)`);
  }
}

main();
